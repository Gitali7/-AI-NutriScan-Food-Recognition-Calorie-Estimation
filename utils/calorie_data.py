def get_calorie_info(food_label):
    # Normalize label
    label_key = food_label.lower().replace(" ", "_")
    
    # Try exact match
    if label_key in calorie_info:
        return calorie_info[label_key]
    
    # Try partial match
    for key in calorie_info:
        if key in label_key:
            return calorie_info[key]
            
    return None
